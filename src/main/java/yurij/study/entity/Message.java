package yurij.study.entity;

/**
 * POJO Message class.
 */
public class Message {
    private String id;
    private String text;

    public Message() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
